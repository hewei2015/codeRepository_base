package hw.learn.beanutils;
public class Order {    
    private User user;    
    private Integer id;    
    private String desc;    
    public String getDesc() {    
        return desc;    
     }    
    public void setDesc(String desc) {    
        this.desc = desc;    
     }    
    public Integer getId() {    
        return id;    
     }    
    public void setId(Integer id) {    
        this.id = id;    
     }    
    public User getUser() {    
        return user;    
     }    
    public void setUser(User user) {    
        this.user = user;    
     }    
        
   
}   
